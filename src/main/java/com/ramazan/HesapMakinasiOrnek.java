public class HesapMakinasiOrnek {
    public int topla(int sayi1, int sayi2) {
        return sayi1 + sayi2;
    }

    public int cikart(int sayi1, int sayi2) {
        return sayi1 - sayi2;
    }

    public int metreKareHesapla(int a, int b) {
        return a * b;
    }
}
